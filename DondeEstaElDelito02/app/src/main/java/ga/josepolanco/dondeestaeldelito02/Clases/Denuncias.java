package ga.josepolanco.dondeestaeldelito02.Clases;

public class Denuncias {
    private String nombre,ubicacion,fecha,tipo_delito,descripcion, urlImagen, urlFotoPerfil, s_latitud, s_longitud;

    public Denuncias(){

    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getTipo_delito() {
        return tipo_delito;
    }

    public void setTipo_delito(String tipo_delito) {
        this.tipo_delito = tipo_delito;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getUrlImagen() {
        return urlImagen;
    }

    public void setUrlImagen(String urlImagen) {
        this.urlImagen = urlImagen;
    }

    public String getUrlFotoPerfil() {
        return urlFotoPerfil;
    }

    public void setUrlFotoPerfil(String urlFotoPerfil) {
        this.urlFotoPerfil = urlFotoPerfil;
    }

    public String getS_latitud() {
        return s_latitud;
    }

    public void setS_latitud(String s_latitud) {
        this.s_latitud = s_latitud;
    }

    public String getS_longitud() {
        return s_longitud;
    }

    public void setS_longitud(String s_longitud) {
        this.s_longitud = s_longitud;
    }

}
